#![allow(non_camel_case_types)]

/// The C signed 32 bit integer type.
pub type c_int = i32;
/// The C signed 64 bit integer type.
pub type c_int64 = i64;
/// The C unsigned 8 bit integer type.
pub type c_uchar = u8;
/// The C unsigned 32 bit integer type.
pub type c_uint = u32;
